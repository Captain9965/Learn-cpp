/*
1. Encapsulation - This is binding data together with the functions that manipulate them
    a byproduct of this is data hiding and abstraction
2. Abstraction - This means displaying only essential information and hiding the details
    An application of this is the use of classes and header files
3. Polymorphism - This is an operation exhibiting different behaviors in different circumstances
    This is dependent on the data being used in the operation. eg:
        a. operator overloading
        b. function overloading
4. Inheritance - This is the capability of a class to derive properties and characteristics from another class.
5. Dynamic binding - The code to be executed at in response to a function call is determined at runtime. This is enabled through virtual functions
6. Message passing - Involves communication, one object to another..
*/